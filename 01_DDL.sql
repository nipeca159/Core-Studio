CREATE TABLE ht_huespedes(
    huesped_id  NUMBER GENERATED BY DEFAULT AS IDENTITY,
    tipo_doc    VARCHAR2(5)     NOT NULL,
    num_doc     VARCHAR2(20)    NOT NULL,
    nombre      VARCHAR2(30)    NOT NULL,
    email       VARCHAR2(30),
    estado      VARCHAR2(15) DEFAULT 'ACTIVO' NOT NULL,

    --CONTRAINTS/RESTRICCIONES

    CONSTRAINT pk_ht_huespedes PRIMARY KEY (huesped_id),
    CONSTRAINT uq_ht_huesped_doc UNIQUE (tipo_doc, num_doc),
    CONSTRAINT ck_ht_huesped_estado CHECK (estado IN ('ACTIVO', 'INACTIVO'))
    );
    
CREATE TABLE ht_habitaciones (
    habitacion_id   NUMBER GENERATED BY DEFAULT AS IDENTITY,
    num_habitacion  VARCHAR2(10) NOT NULL,
    tipo            VARCHAR2(20) NOT NULL,
    precio_noche    NUMBER(10,2) NOT NULL,
    estado          VARCHAR2(15) DEFAULT 'DISPONIBLE' NOT NULL,

    --CONTRAINTS/RESTRICCIONES

    CONSTRAINT pk_ht_habitaciones PRIMARY KEY (habitacion_id),
    CONSTRAINT uq_ht_hab_num UNIQUE (num_habitacion),
    CONSTRAINT ck_ht_hab_estado CHECK (estado IN ('DISPONIBLE', 'RESERVADA')),
    CONSTRAINT ck_hab_precio CHECK (precio_noche >= 0)
    );    
    
CREATE TABLE ht_reservas (
    reserva_id      NUMBER GENERATED BY DEFAULT AS IDENTITY,
    huesped_id      NUMBER NOT NULL,
    habitacion_id   NUMBER NOT NULL,
    fecha_ingreso   DATE NOT NULL,
    fecha_salida    DATE NOT NULL,
    monto_total     NUMBER (12,2) NOT NULL,
    estado          VARCHAR2(10) DEFAULT 'ACTIVA',
 
    --CONTRAINTS/RESTRICCIONES

    CONSTRAINT pk_ht_reservas PRIMARY KEY (reserva_id),
    CONSTRAINT fk_ht_res_huesped FOREIGN KEY (huesped_id)
        REFERENCES ht_huespedes (huesped_id),
    CONSTRAINT fk_ht_res_habitacion FOREIGN KEY (habitacion_id)
        REFERENCES ht_habitaciones (habitacion_id),
    CONSTRAINT ck_ht_res_fecha CHECK (fecha_salida > fecha_ingreso),
    CONSTRAINT ck_ht_res_monto CHECK (monto_total >= 0),
    CONSTRAINT ck_ht_res_estado CHECK (estado IN ('ACTIVA', 'CANCELADA', 'TERMINADA', 'NO LLEGÓ'))
    );     

CREATE TABLE ht_pagos(
    pago_id     NUMBER GENERATED BY DEFAULT AS IDENTITY,
    reserva_id  NUMBER NOT NULL,
    fecha_pago  DATE NOT NULL,
    monto_pago  NUMBER(12,2) NOT NULL,
    canal_pago  VARCHAR2(30) DEFAULT 'TARJETA DE CRÉDITO' NOT NULL,
    referencia  VARCHAR2(50),

    --CONTRAINTS/RESTRICCIONES

    CONSTRAINT pk_ht_pagos PRIMARY KEY (pago_id),
    CONSTRAINT fk_ht_pagos_res FOREIGN KEY (reserva_id)
        REFERENCES ht_reservas (reserva_id),
    CONSTRAINT ck_ht_pago_monto CHECK (monto_pago >= 0)
    );  
    
CREATE TABLE ht_notas_cancelacion (
    nota_id             NUMBER GENERATED BY DEFAULT AS IDENTITY,
    reserva_id          NUMBER NOT NULL,
    fecha_nota          DATE NOT NULL,
    monto_devolucion    NUMBER(12,2) NOT NULL,
    motivo              VARCHAR(30) DEFAULT 'CANCELACION' NOT NULL,

    --CONTRAINTS/RESTRICCIONES

    CONSTRAINT pk_ht_notas PRIMARY KEY (nota_id),
    CONSTRAINT fk_ht_notas_res FOREIGN KEY (reserva_id)
        REFERENCES ht_reservas (reserva_id),
    CONSTRAINT ck_ht_nota_monto CHECK (monto_devolucion >= 0)
    );   

COMMIT;


