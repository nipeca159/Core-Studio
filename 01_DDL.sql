--CREAR NUEVAS TABLAS
--Entrenadores
CREATE TABLE cs_entrenadores (
    entrenador_id     NUMBER GENERATED BY DEFAULT AS IDENTITY,
    nombre_entrenador VARCHAR2(30) NOT NULL,
    --CONTRAINTS/RESTRICCIONES
    CONSTRAINT pk_cs_entrenadores PRIMARY KEY (entrenador_id)
);

--Clientes
CREATE TABLE cs_clientes (
    cliente_id NUMBER GENERATED BY DEFAULT AS IDENTITY,
    tipo_doc   VARCHAR2(5)  NOT NULL,
    num_doc    VARCHAR2(20) NOT NULL,
    nombre     VARCHAR2(30) NOT NULL,
    email      VARCHAR2(30),
    telefono   VARCHAR2(15),
    --CONTRAINTS/RESTRICCIONES
    CONSTRAINT pk_cs_clientes     PRIMARY KEY (cliente_id),
    CONSTRAINT uq_cs_clientes_doc UNIQUE (tipo_doc, num_doc)
);

--Clases
CREATE TABLE cs_clases (
    clase_id      NUMBER GENERATED BY DEFAULT AS IDENTITY,
    entrenador_id NUMBER        NOT NULL,
    nombre_clase  VARCHAR2(50)  NOT NULL,
    dia_semana    VARCHAR2(15)  NOT NULL,
    hora_clase    VARCHAR2(5)   NOT NULL,
    duracion_min  NUMBER        DEFAULT 60 NOT NULL,
    precio        NUMBER(10,2)  NOT NULL,
    estado        VARCHAR2(20)  DEFAULT 'DISPONIBLE' NOT NULL,
    --CONTRAINTS/RESTRICCIONES
    CONSTRAINT pk_cs_clases         PRIMARY KEY (clase_id),
    CONSTRAINT fk_cs_clases         FOREIGN KEY (entrenador_id)
                                    REFERENCES cs_entrenadores (entrenador_id),
    CONSTRAINT uq_cs_clases_horario UNIQUE (dia_semana, hora_clase),
    CONSTRAINT ck_cs_clases_estado  CHECK (estado IN ('DISPONIBLE', 'RESERVADA')),
    CONSTRAINT ck_cs_clases_dia     CHECK (dia_semana IN ('LUNES', 'MARTES', 'MIÉRCOLES', 'JUEVES', 'VIERNES', 'SÁBADO')),
    CONSTRAINT ck_cs_clases_nombre  CHECK (nombre_clase IN ('PILATES REFORMER', 'HOT PILATES', 'BARRÉ', 'HIPOPRESIVOS')),
    CONSTRAINT ck_clases_precio     CHECK (precio >= 0),
    CONSTRAINT ck_clases_duracion   CHECK (duracion_min = 60),
    CONSTRAINT ck_cs_clases_hora    CHECK (REGEXP_LIKE(hora_clase, '^\d{2}:\d{2}$'))
);

--Reservas
CREATE TABLE cs_reservas (
    reserva_id  NUMBER GENERATED BY DEFAULT AS IDENTITY,
    cliente_id  NUMBER        NOT NULL,
    clase_id    NUMBER        NOT NULL,
    monto_total NUMBER(12,2)  NOT NULL,
    estado      VARCHAR2(10)  DEFAULT 'ACTIVA' NOT NULL,
    --CONTRAINTS/RESTRICCIONES
    CONSTRAINT pk_cs_reservas        PRIMARY KEY (reserva_id),
    CONSTRAINT fk_cs_clie_reservas   FOREIGN KEY (cliente_id) 
                                     REFERENCES cs_clientes (cliente_id),
    CONSTRAINT fk_cs_clase_reservas  FOREIGN KEY (clase_id)  
                                     REFERENCES cs_clases (clase_id),
    CONSTRAINT ck_cs_reservas_monto  CHECK (monto_total >= 0),
    CONSTRAINT ck_cs_reservas_estado CHECK (estado IN ('ACTIVA', 'CANCELADA', 'TERMINADA', 'NO LLEGÓ'))
);

--Pagos
CREATE TABLE cs_pagos (
    pago_id    NUMBER GENERATED BY DEFAULT AS IDENTITY,
    reserva_id NUMBER       NOT NULL,
    fecha_pago DATE         NOT NULL,
    monto_pago NUMBER(12,2) NOT NULL,
    canal_pago VARCHAR2(30) DEFAULT 'TARJETA DE CRÉDITO' NOT NULL,
    estado     VARCHAR2(10) DEFAULT 'PAGADO' NOT NULL,
    --CONTRAINTS/RESTRICCIONES
    CONSTRAINT pk_cs_pagos         PRIMARY KEY (pago_id),
    CONSTRAINT fk_cs_pagos_reservas FOREIGN KEY (reserva_id) 
                                    REFERENCES cs_reservas (reserva_id),
    CONSTRAINT uq_cs_pagos_reserva  UNIQUE (reserva_id),
    CONSTRAINT ck_cs_pagos_monto    CHECK (monto_pago >= 0),
    CONSTRAINT ck_cs_pagos_estado   CHECK (estado IN ('PAGADO', 'PENDIENTE')),
    CONSTRAINT ck_cs_pagos_canal    CHECK (canal_pago IN ('TARJETA DE CRÉDITO', 'TRANSFERENCIA', 'EFECTIVO'))
);

--Notas Cancelación
CREATE TABLE cs_notas_cancelacion (
    nota_id          NUMBER GENERATED BY DEFAULT AS IDENTITY,
    reserva_id       NUMBER        NOT NULL,
    fecha_nota       DATE          NOT NULL,
    monto_devolucion NUMBER(12,2)  NOT NULL,
    motivo           VARCHAR2(30)  DEFAULT 'CANCELACION' NOT NULL,
    --CONTRAINTS/RESTRICCIONES
    CONSTRAINT pk_cs_notas         PRIMARY KEY (nota_id),
    CONSTRAINT fk_cs_notas_reservas FOREIGN KEY (reserva_id) 
                                    REFERENCES cs_reservas (reserva_id),
    CONSTRAINT ck_cs_nota_monto     CHECK (monto_devolucion >= 0)
);

